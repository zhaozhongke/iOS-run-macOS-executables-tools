#include <stdbool.h>
bool comparePath(char* a) {
  return a[0] == '/' && a[1] == 'u' && a[2] == 's' && a[3] == 'r' &&
         a[4] == '/' && a[5] == 'l' && a[6] == 'o' && a[7] == 'c' &&
         a[8] == 'a' && a[9] == 'l' && a[10] == '/' && a[11] == 'z' &&
         a[12] == 'h' && a[13] == 'u' && a[14] == 'o' && a[15] == 'w' &&
         a[16] == 'e' && a[17] == 'i';
}
